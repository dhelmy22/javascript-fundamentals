// Equality

    // equal operator       this one is true
    '3' == 3;

    // Strict equality operator    this one is false
     '3' === 3;
     
    //  opposite operator  "bang operator"      NOT equal
    
    // false
    '3' != 3;
    // true
    '3' !== 3;
    
    // Greater than
// false
    3 > 4;

    // Less than
// true
    3 < 4;

    // greaterthan or equal to
    3 >= 4;

    // less than or equal to
    3 <= 4;

    // AND operator


    let x = 13;
    x >= 5 && x <= 33;

    // true && true  = true

    let x = 2;
    x >= 5 && x <= 33;
    // false && true   =  false

    // OR operator    if either side is true, it is true
    x > 3 || x == -3;

    
